/** Aula 03 - Variáveis */

/** Estes tipos de variávcel podem ser reescritas 
 * Let e var 
*/

let nome = "Welington";
let sobrenome = "Souza";
let idade = 40;
// reescrita da variável
idade = 35;

console.log(nome);
console.log(sobrenome);
console.log(idade);

var cidade = "Blumenau";
var estado = "Santa Catarina";
// Reescrita da variável
cidade = "Gaspar";

console.log(cidade);
console.log(estado);

/** O tipo de variável const 
 * Não pode sobreescrever */
const first = "NameFirst";
const second = "NameSecond";

console.log(first);
console.log(second);
